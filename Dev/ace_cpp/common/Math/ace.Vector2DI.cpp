#include "ace.Vector2DI.h"

//----------------------------------------------------------------------------------
//
//----------------------------------------------------------------------------------
namespace ace
{
	//----------------------------------------------------------------------------------
	//
	//----------------------------------------------------------------------------------
	Vector2DI::Vector2DI()
		: X(0)
		, Y(0)
	{

	}

	//----------------------------------------------------------------------------------
	//
	//----------------------------------------------------------------------------------
	Vector2DI::Vector2DI(int32_t x, int32_t y)
		: X(x)
		, Y(y)
	{

	}

	//----------------------------------------------------------------------------------
	//
	//----------------------------------------------------------------------------------
	bool Vector2DI::operator == (const Vector2DI& o)
	{
		return X == o.X && Y == o.Y;
	}

	//----------------------------------------------------------------------------------
	//
	//----------------------------------------------------------------------------------
	bool Vector2DI::operator != (const Vector2DI& o)
	{
		return X != o.X || Y != o.Y;
	}

	//----------------------------------------------------------------------------------
	//
	//----------------------------------------------------------------------------------
}
