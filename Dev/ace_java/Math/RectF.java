package ace;

public class RectF
{
	public float X;
	public float Y;
	public float Width;
	public float Height;

	public RectF()
	{
		X = 0.0f;
		Y = 0.0f;
		Width = 0.0f;
		Height = 0.0f;
	}

	public RectF(float x, float y, float width, float height)
	{
		X = x;
		Y = y;
		Width = width;
		Height = height;
	}
}
